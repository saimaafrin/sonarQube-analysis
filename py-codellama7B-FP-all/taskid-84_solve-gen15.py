def solve(N: int) -> str:
    """Given a positive integer N, return the total sum of its digits in binary.
    
    Example
    >>> solve(1000)
    '1'
    >>> solve(150)
    '110'
    >>> solve(147)
    '1100'
    
    Variables:
        @N integer
             Constraints: 0 â‰¤ N â‰¤ 10000.
    Output:
         a string of binary number
    """

    num_bin = ''
    while N > 0:
        num_bin = str(N % 2) + num_bin
        N //= 2
    
    return num_bin
