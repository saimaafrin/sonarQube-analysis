def int_to_mini_roman(number: int) -> str:
    """
    Given a positive integer, obtain its roman numeral equivalent as a string,
    and return it in lowercase.
    Restrictions: 1 <= num <= 1000

    Examples:
    >>> int_to_mini_roman(19)
    'xix'
    >>> int_to_mini_roman(152)
    'clii'
    >>> int_to_mini_roman(426)
    'cdxxvi'
    """


    roman_digits = {
        1000: 'm',
        900: 'cm',
        500: 'd',
        400: 'cd',
        100: 'c',
        90: 'xc',
        50: 'l',
        40: 'xl',
        10: 'x',
        9: 'ix',
        5: 'v',
        4: 'iv',
        1: 'i',
    }

    mini_roman_num = ''

    while number >= 1:
        for roman_digit, value in roman_digits.items():
            if number >= value:
                mini_roman_num += value
                number -= value
                break
        else:
            raise ValueError(f'Unable to form mini roman numeral for {number}')

    return mini_roman_num
